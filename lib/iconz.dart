  static Future<bool> checkAssetExists(String bldrsThemeAsset) async {
    bool _exists = false;

    final String _path = 'packages/bldrs_theme/$bldrsThemeAsset';

    if (bldrsThemeAsset != null) {
      await rootBundle.load(_path).then((_) {
        _exists = true;
      }).catchError((error) {
        _exists = false;
      });
    }

    return _exists;
  }
